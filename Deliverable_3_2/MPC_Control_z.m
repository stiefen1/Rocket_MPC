            Q = eye(2);
            R = 0.01;
            [~, Qf] = dlqr(mpc.A, mpc.B, Q, R, 0);
            
            obj = 0;
            con = [];
            
            for i=1:N-1
                obj = obj + (x{i}-x_ref)'*Q*(x{i}-x_ref) + (u{i}-u_ref)'*R*(u{i}-u_ref);
